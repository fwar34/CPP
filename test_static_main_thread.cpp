{
	cout << "TangMQ::TangMQ()" << endl;
}

TangMQ::~TangMQ()
{
	sleep(1);
	cout << "TangMQ::~TangMQ()" << endl;
}

bool TangMQ::init()
{
	cout << "TangMQ::init()" << endl;
	return true;
}

